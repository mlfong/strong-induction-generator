package org.stronginduction;
/**
 * Class name: 			MyInt
 * Class description:	Integer autoboxer
 * 						Allows passing by reference
 *  
 * @author 				Man Fong
 *
 */
public class MyInt
{
	//instance properties
	private int value;
	//constructor
	public MyInt(int value)
	{
		this.value = value;
	}
	//accessors
	public int getValue()
	{
		return this.value;
	}
	//mutators
	public void setValue(int value)
	{
		this.value = value;
	}
	//overrides
	public String toString()
	{
		return "" + this.value;
	}
}
